function hexToRgb(hex) {
	if (hex.startsWith('#'))
		hex = hex.slice(1)
	var r = parseInt(hex.substr(0,2), 16)
	var g = parseInt(hex.substr(2,2), 16)
	var b = parseInt(hex.substr(4,2), 16)
	return [r, g, b]
}

